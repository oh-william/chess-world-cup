// Static evaluation from the side-to-move's perspective (negamax convention).
inline int evaluate(const Board& b) {
    int score = 0;
    for (int pt = PAWN; pt <= KING; ++pt) {
        Bitboard w = b.pieces(WHITE, PieceType(pt));
        while (w) { Square s = pop_lsb(w); score += PieceValue[pt] + PST[pt][s ^ 56]; }
        Bitboard bl = b.pieces(BLACK, PieceType(pt));
        while (bl) { Square s = pop_lsb(bl); score -= PieceValue[pt] + PST[pt][s]; }
    }
    return b.side_to_move() == WHITE ? score : -score;
}

